# Verifica si un número entero es positivo, negativo o cero

print("Evalua si un número es entero es positivo, negativo o cero")
num=int(input("Dame un número: "))

if num > 0:
    print("El número es positivo")

if num == 0:
    print("El número es cero")

if num < 0:
    print("El número es negativo")